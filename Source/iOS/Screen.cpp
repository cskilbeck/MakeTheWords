//////////////////////////////////////////////////////////////////////

#include "pch.h"

//////////////////////////////////////////////////////////////////////

namespace Screen
{
	int width = 480;
	int height = 320;
	
	//////////////////////////////////////////////////////////////////////
	
	int Width()
	{
		return width;
	}
	
	//////////////////////////////////////////////////////////////////////
	
	int Height()
	{
		return height;
	}

	//////////////////////////////////////////////////////////////////////
	
	void Init(int width, int height)
	{
		ChangeSize(width, height);
	}
	
	//////////////////////////////////////////////////////////////////////
	
	void Release()
	{
	}
	
	//////////////////////////////////////////////////////////////////////

	void ChangeSize(int newWidth, int newHeight)
	{
		width = newWidth;
		height = newHeight;
		glViewport(0, 0, width, height);
	}
	
	//////////////////////////////////////////////////////////////////////
	
	bool Update()
	{
		return true;
	}
	
	//////////////////////////////////////////////////////////////////////
	
	void Clear(Color color)
	{
		float f[4];
		color.GetFloats(f);
		glClearColor(f[0], f[1], f[2], f[3]);
		glClear(GL_COLOR_BUFFER_BIT);
	}
	
	//////////////////////////////////////////////////////////////////////
	
	void Present()
	{
	}
}

