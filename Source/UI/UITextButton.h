namespace UI
{

	//////////////////////////////////////////////////////////////////////

	struct TextButton : TextLabel, Button
	{
		//////////////////////////////////////////////////////////////////////

		TextButton(Font *font, string text, Vec2 pos, Color color)
			: Item(pos)
			, Button()
			, TextLabel(font, text, pos)
		{
			mColor = color;
			CalculateHitRect();
		}

		//////////////////////////////////////////////////////////////////////

		void Draw(SpriteList *spriteList) override;
		void CalculateHitRect() override;

		//////////////////////////////////////////////////////////////////////
	};
}